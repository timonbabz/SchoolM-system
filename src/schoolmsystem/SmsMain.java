        public void addUserMethod(){
            
            String useridentity = txtId.getText();
            fname = txtFname.getText();
            lname = txtLname.getText();
            String username = txtUsernmae.getText();
            String password = new String(txtPassword.getPassword());
            String confPassword = new String(txtConfirmPassword.getPassword());
            permission = (String) jComboPermissions.getSelectedItem();
            designation = (String) jComboDesignation.getSelectedItem();
            
            try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/school_management?useSSL = false", "root", "");
            state = con.prepareStatement("INSERT INTO users (id_number,fname,lname,username,password,permissions,designation) VALUES (?,?,?,?,?,?,?)");

            state.setString(1, useridentity);
            state.setString(2, fname);
            state.setString(3, lname);
            state.setString(4, username);
            
            if(md5(txtConfirmPassword.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Password must be confirmed");
            txtConfirmPassword.requestFocus();
            }
            state.setString(5, md5(txtConfirmPassword.getPassword()));
            state.setString(6, permission);
            state.setString(7, designation);

            int count = state.executeUpdate();
            if(count > 0){
                JOptionPane.showMessageDialog(null, fname+" "+lname+" added as user with username "+username);
            }
            }catch(ClassNotFoundException | SQLException | HeadlessException e){
        JOptionPane.showMessageDialog(null, e.getMessage());
        }
        }

private String md5(char [] c){
    try{
    MessageDigest digs = MessageDigest.getInstance("MD5"); 
    digs.update((new String(c)).getBytes("UTF8"));
    String tsr = new String(digs.digest());
    return tsr;
    
    }catch(NoSuchAlgorithmException | UnsupportedEncodingException dd){
    return "";
    }

}

